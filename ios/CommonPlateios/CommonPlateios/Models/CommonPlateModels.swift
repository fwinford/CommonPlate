import Foundation

struct DiningSpot: Identifiable, Hashable {
    var id: String { name }
    let name: String
    /// Nil when the address is unavailable, e.g. a backend-sourced request whose
    /// wire representation is only a `vendor` name string. Not a fabricated "".
    let address: String?
}

enum RequestTiming: String, CaseIterable, Identifiable {
    case asap = "ASAP"
    case later = "Later"

    var id: String { rawValue }
}

/// Canonical lifecycle status. Backend wire values `requested`/`claimed`/`placed`
/// map to `.open`/`.claimed`/`.placed` — see RequestDTOs.swift for the wire mapping.
enum RequestStatus: String {
    case open = "Open"
    case claimed = "Claimed"
    case placed = "Placed"
}

/// Canonical backend-backed request model. `id`, `createdAt`, `expiresAt`, and `status`
/// are backend-owned truth (see docs/week-2-integration-spec.md); they are never
/// computed or generated by iOS in the production path. Requester-private data
/// (pickup name, email, phone) is intentionally not modeled here — the public
/// wire shape never carries it, and a successful claim's pickup name belongs to
/// `RequestStore`'s active-claim state, not this model.
struct FoodRequest: Identifiable {
    let id: String
    let diningSpot: DiningSpot
    let foodDescription: String
    let pickupWindowText: String
    let windowStart: Date?
    let windowEnd: Date?
    let timing: RequestTiming
    let preferredPickupTime: Date?
    let createdAt: Date
    let expiresAt: Date
    var status: RequestStatus

    /// All backend-owned values are supplied explicitly and never generated
    /// locally in the production path.
    init(
        id: String,
        diningSpot: DiningSpot,
        foodDescription: String,
        pickupWindowText: String,
        windowStart: Date?,
        windowEnd: Date?,
        createdAt: Date,
        expiresAt: Date,
        status: RequestStatus
    ) {
        self.id = id
        self.diningSpot = diningSpot
        self.foodDescription = foodDescription
        self.pickupWindowText = pickupWindowText
        self.windowStart = windowStart
        self.windowEnd = windowEnd
        self.timing = windowStart != nil || windowEnd != nil ? .later : .asap
        self.preferredPickupTime = windowStart ?? windowEnd
        self.createdAt = createdAt
        self.expiresAt = expiresAt
        self.status = status
    }

    var isExpired: Bool {
        Date() >= expiresAt
    }

    var isActive: Bool {
        status == .open && !isExpired
    }

    var shouldShowInASAPSection: Bool {
        switch timing {
        case .asap:
            return true

        case .later:
            guard let preferredPickupTime else {
                return false
            }

            let oneHourFromNow = Date().addingTimeInterval(60 * 60)
            return preferredPickupTime <= oneHourFromNow
        }
    }

    var timingDescription: String {
        pickupWindowText
    }

    var listTimingDescription: String {
        pickupWindowText
    }
}

/// Temporary local-simulation-only fulfillment state for the currently
/// unconnected SwiftUI prototype. It is never part of `FoodRequest`, a DTO, or
/// `RequestService`, and it carries no claim credentials.
struct LocalSimulationFulfillmentDetails {
    let helperEmail: String
    let helperPhoneNumber: String
    let orderConfirmation: String
    let pickupTimeOrETA: String
    let noteToRequester: String
    let submittedAt = Date()
}

/// Temporary wrapper used only by the unconnected local SwiftUI simulation.
/// Production request truth remains the nested canonical `FoodRequest`; the
/// wrapper holds only compatibility state needed by the prototype flow. It is
/// not constructed by DTO mapping or `RequestService` and never stores a claim
/// token. Requester email and phone remain in `RequestFoodView` form state and
/// are intentionally not retained here.
struct LocalSimulatedRequest: Identifiable {
    var canonicalRequest: FoodRequest
    let pickupName: String
    var fulfillmentDetails: LocalSimulationFulfillmentDetails?

    var id: String { canonicalRequest.id }

    init(
        diningSpot: DiningSpot,
        foodDescription: String,
        pickupName: String,
        timing: RequestTiming,
        preferredPickupTime: Date?
    ) {
        let createdAt = Date()
        let windowStart = timing == .later ? preferredPickupTime : nil
        let pickupWindowText: String
        let expiresAt: Date

        switch timing {
        case .asap:
            pickupWindowText = "ASAP"
            expiresAt = createdAt.addingTimeInterval(5 * 60 * 60)
        case .later:
            if let preferredPickupTime {
                pickupWindowText = "Around \(preferredPickupTime.formatted(date: .omitted, time: .shortened))"
            } else {
                pickupWindowText = "Later today"
            }
            expiresAt = (preferredPickupTime ?? createdAt).addingTimeInterval(5 * 60 * 60)
        }

        self.canonicalRequest = FoodRequest(
            id: UUID().uuidString,
            diningSpot: diningSpot,
            foodDescription: foodDescription,
            pickupWindowText: pickupWindowText,
            windowStart: windowStart,
            windowEnd: nil,
            createdAt: createdAt,
            expiresAt: expiresAt,
            status: .open
        )
        self.pickupName = pickupName
        self.fulfillmentDetails = nil
    }

    var listTimingDescription: String {
        if canonicalRequest.shouldShowInASAPSection,
           canonicalRequest.timing == .later,
           let preferredPickupTime = canonicalRequest.preferredPickupTime {
            return "Due around \(preferredPickupTime.formatted(date: .omitted, time: .shortened))"
        }

        return canonicalRequest.timingDescription
    }
}
